<h3>About Tuppi</h3>
<p>Tuppi is a tool for learning Hittite cuneiform.</p>

<h3>What is Hittite?</h3>
<p>
	Hittite is an ancient language spoken in Anatolia (modern-day Turkey) from the 18th to 12th
	centuries BCE. It was written in a cuneiform script, which was used to write other languages in
	the region, such as Luwian and Aramaic. The Hittite language is closely related to Luwian, and
	both are related to the Indo-European languages.
</p>
